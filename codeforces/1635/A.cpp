ll power(ll x, ll y) {
    ll res = 1;
    
    while (y) {
        if (y % 2 == 1)
            res = res * x%mod;
       
        y = y/2;
       
        x = (x * x)%mod;
    }
    return res;
}

bool isPerfectSquare(ll x) {
    ll left = 1, right = x;
   
    while (left <= right) {
        ll mid = (left + right) / 2;
       
        if (mid * mid == x) {
            return true;
        }
       
        if (mid * mid < x) {
            left = mid + 1;
        }
        
        else {
            right = mid - 1;
        }
    }
    return false;
}

ll lcm(ll a, ll b) {
    return (a / __gcd(a, b)) * b;
}

void solve() {
    int n;
    cin>>n;

    int freq[30] = {0};

    for (int i=0;i<n;i++) {
        int x;
        cin>>x;
        int j = 0;
        while (x > 0) {
            freq[j] += x&1;
            j++;
            x >>= 1; 
        }
    }
    long long sum = 0;

    for (int i = 0;i<30;i++) {
        if (freq[i] > 0) sum += pow(2, i);
    }

    cout<<sum<<endl;
}


