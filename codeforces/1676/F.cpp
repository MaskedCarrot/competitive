ll power(ll x, ll y) {
    ll res = 1;
    
    while (y) {
        if (y % 2 == 1)
            res = res * x%mod;

        y = y/2;

        x = (x * x)%mod;
    }
    return res;
}

bool isPerfectSquare(ll x) {
    ll left = 1, right = x;
   
    while (left <= right) {
        ll mid = (left + right) / 2;
       
        if (mid * mid == x) {
            return true;
        }
       
        if (mid * mid < x) {
            left = mid + 1;
        }
        
        else {
            right = mid - 1;
        }
    }
    return false;
}

ll lcm(ll a, ll b) {
    return (a / __gcd(a, b)) * b;
}

void solve() {
    int n;
    cin>>n;
    int k;
    cin>>k;
    map<ll,ll> m;
    vector<ll> v;
    for (int i=0;i<n;i++) {
        ll x;
        cin>>x;
        if (m[x] == 0) v.push_back(x);
        m[x]++;
    }

    sort(all(v));

    ll len = v.size();

    vector<bool> dp(len, false);

    for (ll i=0;i<len;i++) {
        dp[i] = ((m[v[i]] >= k));
    }

    ll tl = INT_MAX;
    ll tr = INT_MIN;
    ll l = tl;
    ll r = tr;
    for (int i=0;i<len;i++) {
        if (i > 0 && v[i]-v[i-1] > 1) {
            tl = INT_MAX;
            tr = INT_MIN;
        }
        if (dp[i]) {
            if (tl == INT_MAX) tl = v[i];
            tr = v[i];
        } else {
            tl = INT_MAX;
            tr = INT_MIN;
        }
        if (tr-tl+1 > r-l+1) {
            r = tr;
            l = tl;
        }
    }

    if (tr-tl+1 > r-l+1) {
            r = tr;
            l = tl;
    }

    if (l == INT_MAX) cout<<-1<<endl;
    else cout<<l<<' '<<r<<endl;
    
}

